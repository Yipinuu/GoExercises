package main

func main() {
	var first int = 10
	var cond int

	if first <= 0 {
		print("first is less than or equal to 0\n")
	} else if first > 0 && first < 5 {
		print("first is between 0 and 5\n")
	} else {
		print("first is 5 or greater\n")
	}
	if cond = 5; cond > 10 {
		print("cond is greater than 10\n")
	} else {
		print("cond is not greater than 10\n")
	}
}
